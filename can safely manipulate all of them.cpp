#include <bits/stdc++.h>
using namespace std;

int main() {
    int t,x,y;
    cin>>t;
    while(t--){
        cin>>x>>y;
        if(x<y){
            cout<<"NO"<<endl;
        }
        else if(x==y){
            cout<<"YES"<<endl;
        }
        else{
            cout<<"YES"<<endl;
        }
    }
    return 0;
}
    
